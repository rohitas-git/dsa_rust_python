
# Tree Data Structure
# - Unordered fragmented memory 
# - Non-linear data structure 
    # - Stores data in hierarchical fashion 
        #   unlike sequential fashion in linear data structure 
        #   which are iterative in nature 
# - To represent Hierarchy 
    # - Company structure
    # - Folder structure
# - Recursive in nature

    

# TERMS
# Root Node [Node at top of the tree]
# Leaf Node [Node at bottom of the tree]
# Children 
# Parent

# Descendants of a node [All nodes that lie in a subtree with this node as root]

# Degree of a node [Number of children this node has]
    # - Leaf node have degree 0
    # - Those nodes with degree != 0, called internal nodes